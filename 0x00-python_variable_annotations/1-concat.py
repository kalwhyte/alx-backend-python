#!/usr/bin/env python3
"""
Module for Basic annotations - concat
"""


def concat(str1: str, str2: str) -> str:
    """
    Returns the concatenation of two strings.

    Args:
        str1 (str): First string.
        str2 (str): Second string.

    Returns:
        str: The concatenation of the two strings.
    """
    return str1 + str2
