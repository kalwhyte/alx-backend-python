#!/usr/bin/env python3
"""
This module contains the function floor which takes a float n as argument
and returns the floor of the float.
"""

import math


def floor(n: float) -> int:
    """
    Return the floor of the float.

    Args:
        n (float): The float to floor.

    Returns:
        int: The floored float.
    """
    return math.floor(n)
